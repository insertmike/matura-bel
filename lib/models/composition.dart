import 'package:flutter/foundation.dart';

class Composition {
  final int id;
  final String name;
  final String genre;
  final String mainSubject;
  final List<String> artisticShapes;

  Composition(
      {
      @required this.id,
      @required this.name,
      @required this.genre,
      @required this.mainSubject,
      @required this.artisticShapes
      });
}
